package com.vernos.sqorr.model;

public class PlayersStatistics {
    String playing_positions;
    String first_player_points;
    String second_player_points;

    public String getPlaying_positions() {
        return playing_positions;
    }

    public void setPlaying_positions(String playing_positions) {
        this.playing_positions = playing_positions;
    }

    public String getFirst_player_points() {
        return first_player_points;
    }

    public void setFirst_player_points(String first_player_points) {
        this.first_player_points = first_player_points;
    }

    public String getSecond_player_points() {
        return second_player_points;
    }

    public void setSecond_player_points(String second_player_points) {
        this.second_player_points = second_player_points;
    }
}
